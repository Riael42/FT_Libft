#include "libft.h"

void	ft_putunbr(unsigned long nb)
{
	if (nb % 10 == nb)
	{
		ft_putchar (nb + '0');
		return ;
	}
	ft_putunbr(nb / 10);
	ft_putunbr(nb % 10);
}
